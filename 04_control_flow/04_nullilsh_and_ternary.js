//* Nullish Coalescing Operator (??): null , undefined

let val1 = 11 ?? 22
console.log(val1)

let val2 = null ?? 33 ?? 66
console.log(val2)

let val3 = undefined ?? 55 ?? 77
console.log(val3)

//* Ternary Operator => ?
//* condition ? true : false

